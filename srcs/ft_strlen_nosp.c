#include "libft.h"

int		ft_strlen_nosp(const char *s)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		i++;
	while (s[i] != '\0')
	{
		n++;
		i++;
	}
	i--;
	if (n > 0)
	{
		while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		{
			i--;
			n--;
		}
	}
	return (n);
}
